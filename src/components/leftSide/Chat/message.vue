<script>
  import conf from '../../../modules/conf/index.js';
  export default {
    data () {
      return {
        imgSrc: conf.serverUrl + conf.userAPIMethods.mediaGava + '/' + this.$store.getters.user.image
      };
    },
    computed: {
      session () {
        // bus.on('next', data => {
        const list = this.$store.getters.sessions;
        console.log('list  ', list);
        console.log(document.location.pathname.split('/')[2]);
        for (let i = 0; i < list.length; ++i) {
          if (list[i].id === document.location.pathname.split('/')[2]) {
            return list[i];
          }
        }
        // });
      }
  },
    methods: {
    },
    filters: {
      time (date) {
        if (typeof date === 'string') {
          date = new Date(date);
        }
        // return '23:00';
        return date.getHours() + ':' + date.getMinutes();
      }
  },
    directives: {
      scroll_bottom() {
        this.vm.$nextTick(() => {
          this.el.scrollTop = this.el.scrollHeight - this.el.clientHeight;
        });
      }
  }
};
</script>

<template>
<div class="message">
    <ul v-if="session">
        <li v-for="item in session.messages">
            <p class="time">
                <span>{{ item.date | time }}</span>
            </p>
            <div class="main" :class="{ self: item.self }">
                <img class="avatar" width="30" height="30" :src="item.self ? imgSrc : session.user.img" />
                <div class="text">{{ item.content }}</div>
            </div>
        </li>
    </ul>
</div>
</template>

